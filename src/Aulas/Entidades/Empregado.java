package Aulas.Entidades;

public class Empregado {


    public double NetSalary(double taxa, double salario)
    {
        return salario = (salario - taxa);
    }

    public double AumentoDoSalarioEmPersentagem(double salario, double percentagem, double salarioL)
    {
        return salarioL = (((percentagem/100)* salario) + salarioL);
    }
}
